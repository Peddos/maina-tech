// Function to check if user has access to a package
function checkPackageAccess(userId, system, subject) {
  try {
    const accessData = JSON.parse(localStorage.getItem('packageAccess') || '{}');
    const packageKey = `${system}_${subject}`;
    const packageAccess = accessData[packageKey];
    
    if (!packageAccess) return false;
    
    const expiryDate = new Date(packageAccess.expiryDate);
    const now = new Date();
    
    // Check if access has expired
    if (now > expiryDate) {
      // Remove expired access
      delete accessData[packageKey];
      localStorage.setItem('packageAccess', JSON.stringify(accessData));
      return false;
    }
    
    return true;
  } catch (error) {
    console.error('Error checking package access:', error);
    return false;
  }
}

// Function to handle package purchase
function handlePackagePurchase(userId, system, subject) {
  try {
    const accessData = JSON.parse(localStorage.getItem('packageAccess') || '{}');
    const packageKey = `${system}_${subject}`;
    
    // Set 30 days access
    const expiryDate = new Date();
    expiryDate.setDate(expiryDate.getDate() + 30);
    
    accessData[packageKey] = {
      active: true,
      purchaseDate: new Date().toISOString(),
      expiryDate: expiryDate.toISOString(),
      transactionVerified: false // This will be set to true after manual verification
    };
    
    localStorage.setItem('packageAccess', JSON.stringify(accessData));
    return true;
  } catch (error) {
    console.error('Error handling purchase:', error);
    return false;
  }
}

